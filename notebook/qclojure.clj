;; # QClojure
;; QClojure is a Clojure library for building and simulating quantum algorithms.
;; 
;; ## Usage
;; To use QClojure, you can include it in your Clojure project as a dependency.
;;
;; If you are using Leiningen, add the following dependency to your `project.clj` file:
;; ```clojure
;; [org.soulspace/qclojure "0.1.0-SNAPSHOT"]
;; ```
;;
;; If you are using Clojure CLI, add the following to your `deps.edn` file:
;; ```clojure
;; {:deps {org.soulspace/qclojure {:mvn/version "0.1.0-SNAPSHOT"}}}
;; ```
